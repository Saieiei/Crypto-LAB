def bin2hex(s):
    mp = {"0000": '0', "0001": '1', "0010": '2', "0011": '3',
          "0100": '4', "0101": '5', "0110": '6', "0111": '7',
          "1000": '8', "1001": '9', "1010": 'A', "1011": 'B',
          "1100": 'C', "1101": 'D', "1110": 'E', "1111": 'F'}
    hexadecimal = ""
    for i in range(0, len(s), 4):
        chunk = ""
        chunk += s[i]
        chunk += s[i + 1]
        chunk += s[i + 2]
        chunk += s[i + 3]
        hexadecimal += mp[chunk]

    return hexadecimal


